public class Flight {

    private String name;
    private String orig;
    private String dest;
    private int year;
    private int month;
    private int day;
    private String id;

    public Flight(String name, String orig, String dest, int year, int month, int day, String id)
    {
        this.name = name;
        this.orig = orig;
        this.dest = dest;
        this.year = year;
        this.month = month;
        this.day = day;
        this.id = id;
    }

}
